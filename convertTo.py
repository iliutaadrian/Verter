CURRENCIES_BY_SYMBOL = {
    'USD': ['$', 'dollar', 'buck'],
    'EUR': ['€', 'euro'],
    'RON': ['lei'],
    'JPY': ['¥']
}


def verify_is_symbol(value):
    for k, v in CURRENCIES_BY_SYMBOL.items():
        for item in v:
            if value.find(item) != -1:
                return k
    return None


def verify_is_value(value):
    sorted(CURRENCIES_BY_COUNTRY_CODE.items(), key=lambda x: x[1])
    return [True for v in CURRENCIES_BY_COUNTRY_CODE.values() if v == value]


def get_all():
    return [v for v in CURRENCIES_BY_COUNTRY_CODE.values() if v not in ('RON', 'EUR')]


def get_by_country(country_code, default=None):
    currencies = CURRENCIES_BY_COUNTRY_CODE.get(country_code)
    if not currencies:  # it could be an empty tuple
        return default
    else:
        return currencies
